DROP TABLE matches;
DROP TABLE teams;

CREATE TABLE teams (
  team_id INT,
  team_name VARCHAR(200),
  CONSTRAINT pk_team
    PRIMARY KEY (team_id)
);

CREATE TABLE matches (
  home_team_id INT,
  away_team_id INT,
  home_team_goals INT,
  away_team_goals INT,
  CONSTRAINT pk_matches
    PRIMARY KEY (home_team_id, away_team_id),
  CONSTRAINT fk_match_home
    FOREIGN KEY (home_team_id)
    REFERENCES teams(team_id),
   CONSTRAINT fk_match_away
    FOREIGN KEY (home_team_id)
    REFERENCES teams(team_id)
);

